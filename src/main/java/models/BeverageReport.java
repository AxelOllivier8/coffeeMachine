package models;

public class BeverageReport {

  private int coffee;

  private int tea;

  private int chocolate;

  private int orange;

  private int totalCent;

  public int getCoffee() {
    return coffee;
  }

  public void setCoffee(int coffee) {
    this.coffee = coffee;
  }

  public int getTea() {
    return tea;
  }

  public void setTea(int tea) {
    this.tea = tea;
  }

  public int getChocolate() {
    return chocolate;
  }

  public void setChocolate(int chocolate) {
    this.chocolate = chocolate;
  }

  public int getOrange() {
    return orange;
  }

  public void setOrange(int orange) {
    this.orange = orange;
  }

  public int getTotalCent() {
    return totalCent;
  }

  public void setTotalCent(int totalCent) {
    this.totalCent = totalCent;
  }
}
